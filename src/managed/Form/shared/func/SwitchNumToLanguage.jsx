export default SwitchNumToLanguage = (languageNum) => {
  switch (languageNum) {
    case 1:
      return "國語";
    case 2:
      return "台語";
    case 3:
      return "英文";
    case 4:
      return "英文";
    case 5:
      return "越南文";
    case 6:
      return "泰文";
    case 7:
      return "印尼語";
    case 8:
      return "菲律賓語";
    default:
      return "";
  }
};
